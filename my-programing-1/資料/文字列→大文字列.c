#include <stdio.h>

int main(void)
{
    // 文字列str: 変換元。十分なサイズで宣言する
    char str[] = "abcDEF";
    // 文字列str2: 変換後の文字列を格納する配列
    char str2[10]; 

    int i; 

    // for文を用いてstrの先頭からナル文字(\0)に到達するまで繰り返す
    // 文字列はchar型の配列であり、終端にナル文字(\0)が必要 [2]
    for (i = 0; str[i] != '\0'; i++) { 
        
        // str[i]の文字が小文字（'a'から'z'）の範囲にあるか確認する [5, 11, 12]
        if (str[i] >= 'a' && str[i] <= 'z') {
            
            // 小文字を大文字に変換してstr2にコピーする
            // 'a'と'A'の文字コードの差分を引くことで大文字になる
            // 'a'を97、'A'を65として扱う（文字定数はコンパイル時に数値になる）
            str2[i] = str[i] - ('a' - 'A');
            
        } else {
            
            // それ以外（もともと大文字やその他の文字）はそのままコピーする
            str2[i] = str[i];
        }
    }

    // 最後に、str2の終端にナル文字（\0）を必ず追加する [2]
    str2[i] = '\0'; 

    printf("文字列 str = %s\n", str);
    printf("大文字に変換した文字列 str2 = %s\n", str2);

    return 0;
}
