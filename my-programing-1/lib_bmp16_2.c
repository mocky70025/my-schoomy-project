//=====================================================
// 左上(x0,y0)からドット絵を描く（拡大率scaleでドット描画）
//=====================================================
void bmp16_draw_dote(int x0, int y0, int scale)
{
  for (int y = 0; y < 16; y++) {
    for (int x = 0; x < 16; x++) {
      int color = a[y][x];
      int x1 = x0 + scale * x;
      int y1 = x0 + scale * y;
      // ここをbmp16_fill_rectを呼び出して1ドットを描いてください
      bmp16_fill_rect(x1, y1, x1+scale, y1+scale, color); 
    

    }
  }
}

int main(void)
{
  int scale = 8; // 一つのドットの大きさ（拡大率）

  bmp16_create(16 * scale, 16 * scale, 15); // 背景を白で作成


  bmp16_palette_set_rgb(253, 221, 190,  5); // 裏(0)は灰っぽく
  bmp16_palette_set_rgb(255,   1,  0,  10); // 枠(1)は黒っぽく
  bmp16_palette_set_rgb(255, 255, 255, 11); // 体(11)はピンク
  bmp16_palette_set_rgb(0,   0,  0,  1); // 赤(5)は赤っぽく

  bmp16_draw_dote(0, 0, scale); // 左上(0,0)から拡大率scaleでドット絵

  bmp16_save("bmp_dote.bmp");  // 画像を保存
  system("open bmp_dote.bmp"); // 画像を開く

  return 0;
}
